public class Leaf {
    
    private float freq;
    private char key;

    public Leaf(float freq, char key) {
        this.freq = freq;
        this.key = key;
    }
    public float getFreq() {
        return freq;
    }
    public void setFreq(float freq) {
        this.freq = freq;
    }
    public char getKey() {
        return key;
    }
    public void setKey(char key) {
        this.key = key;
    }
}
